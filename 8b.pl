chop(@_ = <STDIN>);
for (0 .. $#_) {
  if ($_[$_] =~ /\S/) {
    $r[$_] = [split(//, $_[$_])];
  }
}

for $r (1 .. $#_ - 1) {
  for $c (1 .. $#{$r[$r]} - 1) {
    for ($x = $c - 1, $s = 1;
         $x > 0 && $r[$r]->[$x] < $r[$r]->[$c];
         $s++, $x--) { }
    for ($x = $c + 1, $l = 1;
         $x < $#{$r[$r]} && $r[$r]->[$x] < $r[$r]->[$c];
         $l++, $x++) { }
    for ($y = $r - 1, $s *= $l, $l = 1;
         $y > 0 && $r[$y]->[$c] < $r[$r]->[$c];
         $l++, $y--) { }
    for ($y = $r + 1, $s *= $l, $l = 1;
         $y < $#_ && $r[$y]->[$c] < $r[$r]->[$c];
         $l++, $y++) { }
    $m = $s * $l if $s * $l > $m;
  }
}
print $m
